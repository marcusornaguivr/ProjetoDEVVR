    private void carregaTabela() {
        tabela.addColumn("Codigo");
        tabela.addColumn("Nome");
        tabela.addColumn("Login");
        tblAluno.setRowHeight(25);
        tblAluno.setGridColor(Color.WHITE);
        tblAluno.setDefaultRenderer(Integer.class, new TableCellRender());
        tblAluno.setDefaultRenderer(Object.class, new TableCellRender());
    }

    private void consultar() {
        usuario = new Usuario();
        tabela = new TableModel();
        
        carregaTabela();
        List<Usuario> lista = new ArrayList<>();

        if (!txtNome.getText().isEmpty()) {
            usuario.setNome(txtNome.getText());
        }
        if (!txtCodigo.getText().isEmpty()) {
            usuario.setCodigo(Integer.parseInt(txtCodigo.getText()));
        }

        for (Usuario u : controller.get(usuario)) {
            tabela.addRow(new Object[]{
                u.getCodigo(),
                u.getNome(),
                u.getLogin(),});
        }
        tblAluno.setModel(tabela);
    }
    
    private void editar(){
        usuario = new Usuario();
        int linhaSelecionada = tblAluno.getSelectedRow();

        if (linhaSelecionada >= 0) {
            usuario.setCodigo(Integer.parseInt(tblAluno.getValueAt(linhaSelecionada, 0).toString()));
            usuario = controller.buscarUsuarioPorCodigo(usuario);
            CadastroUsuario frmCadastro = new CadastroUsuario(usuario);
            this.getParent().add(frmCadastro);
            frmCadastro.setVisible(true);
        }
    }
    
    private void excluir(){
        usuario = new Usuario();
        int linhaSelecionada = tblAluno.getSelectedRow();

        if (linhaSelecionada >= 0) {
            usuario.setCodigo(Integer.parseInt(tblAluno.getValueAt(linhaSelecionada, 0).toString()));
            if(controller.deletaUsuario(usuario)){
                JOptionPane.showMessageDialog(null, "Excluido com sucesso!");
                tabela.removeRow(linhaSelecionada);
            }else{
                JOptionPane.showMessageDialog(null, "Erro ao excluir!");
            }
        }
    }
    
    private void incluir(){
        CadastroUsuario cadastro = new CadastroUsuario();
        this.getParent().add(cadastro);
        cadastro.setVisible(true);
    }

