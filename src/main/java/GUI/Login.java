    private void autenticar() {
        usuario = new Usuario();

        if (!txtLogin.getText().isEmpty()) {
            usuario.setLogin(txtLogin.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Dados preenchidos incorretamente!");
            txtLogin.requestFocus();
            return;
        }
        if (!String.valueOf(txtSenha.getPassword()).isEmpty()) {
            usuario.setSenha(String.valueOf(txtSenha.getPassword()));
        } else {
            JOptionPane.showMessageDialog(null, "Dados preenchidos incorretamente!");
            txtSenha.requestFocus();
            return;
        }

        if (usuarioController.validaUsuario(usuario)) {
            Principal p = new Principal();
            p.setVisible(true);
            sair();
        } else {
            JOptionPane.showMessageDialog(null, "Usuario ou senha incorretos!");
            txtLogin.requestFocus();
        }
    }

    private void sair() {
        this.dispose();
    }

